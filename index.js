//เมื่อกด addBtn จะรวมค่าใน total แล้วไปแสดงที่ result
const resultEle = document.getElementById('result');
const diffEle = document.getElementById('diff');
document.getElementById("addBtn").addEventListener('click', function () {
    let sum = 0;
    for (let i = 0; i < total.length; i++) {
        sum = sum + total[i];
    }

    resultEle.textContent = sum;
    // diffEle.textContent = "ส่วนต่าง: " + (sum - price).toString();
    // ถ้า ส่วนต่าง น้อยกว่า 0 ให้แสดงคำว่า ขาดทุน: xxx บาท
    // ถ้า ส่วนต่าง มากกว่า 0 ให้แสดงคำว่า กำไร: xxx บาท
    // else แสดง เท่าทุน !!
    const diff = sum - price;
    if (diff < 0) {
        diffEle.textContent = `ขาดทุน: ${Math.abs(diff)} บาท`;
    } else if (diff > 0) {
        diffEle.textContent = `กำไร: ${Math.abs(diff)} บาท`;
    } else {
        diffEle.textContent = "เท่าทุน !!"
    }
});

// เมื่อกด clearBtn จะล้างค่าใน total และ result
document.getElementById("clearBtn").addEventListener('click', function () {
    total = [];
    updateEquation(total);
    resultEle.textContent = "0";
    diffEle.textContent = "";
});